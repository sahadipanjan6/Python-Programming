''' Question: Write a Python program to test if a variable is a list or tuple or a set.

Solution: '''

variable1 = [1, 2, 3]
variable2 = (2, 5)
variable3 = set([1, 2, 2, 3])

print("Type of variable1: ", type(variable1))
print("Type of variable2: ", type(variable2))
print("Type of variable3: ", type(variable3))
